package com.neusoft.his.entity;

public class ConstantItem {

	private  int constantItemId;
	
	private int constantTypeId;
	
	private String constantCode;
	
	private String constantName;
	
	private int delMark;

	public int getConstantItemId() {
		return constantItemId;
	}

	public void setConstantItemId(int constantItemId) {
		this.constantItemId = constantItemId;
	}

	public int getConstantTypeId() {
		return constantTypeId;
	}

	public void setConstantTypeId(int constantTypeId) {
		this.constantTypeId = constantTypeId;
	}

	public String getConstantCode() {
		return constantCode;
	}

	public void setConstantCode(String constantCode) {
		this.constantCode = constantCode;
	}

	public String getConstantName() {
		return constantName;
	}

	public void setConstantName(String constantName) {
		this.constantName = constantName;
	}

	public int getDelMark() {
		return delMark;
	}

	public void setDelMark(int delMark) {
		this.delMark = delMark;
	}
	
	
	
}
